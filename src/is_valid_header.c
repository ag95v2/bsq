int	is_digit(char c)
{
	return ((c <= '9' && c >=  '0')? 1 : 0);
}

int	only_digits(char *s)
{
	while (*s)
	{
		if (!is_digit(*s))
			return (0);
		s++;
	}
	return (1);
}
